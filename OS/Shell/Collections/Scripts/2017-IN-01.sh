#!/bin/bash

if [[ $# -ne 3 ]] ; then
    echo "Expected 3 arguments"
    exit 1
fi

args1=$(cat "${1}" | egrep  "^${2}=" | awk -F '=' '{print $2}' | tr ' ' '\n')
args2=$(cat "${1}" | egrep  "^${3}=" | awk -F '=' '{print $2}' | tr ' ' '\n')

while read line ; do
    echo "${args1}" | grep -q -F "${line}"
    if [[ ${?} -ne 0 ]] ; then
                result+=$(echo "${line} ")
        fi
done < <(echo "${args2}")

sed -i -E "s/(${3}=).*/\1${result}/g" ${1}
