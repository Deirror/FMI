#!/bin/bash

if [[ $# -ne 3 ]]; then
    echo "Exactly three arguments required"
    exit 1
fi

FILE="$1"
STR1="$2"
STR2="$3"

if [[ ! -f "$FILE" ]]; then
    echo "File doesn't exist"
fi

LINE1="$(grep "$STR1")"
LINE2="$(grep "$STR2")"

if [[ -z "$LINE1" || -z "$LINE2" ]]; then
    echo "Strings not found in the file"
    exit 1
fi

ARGS1=$(cut -d '=' -f 2 < <(echo "$LINE1"))
ARGS2=$(cut -d '=' -f 2 <<< "$LINE2")

